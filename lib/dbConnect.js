import mongoose from "mongoose";

const LOCAL_MONGODB_URI = process.env.LOCAL_MONGODB_URI;
const REMOTE_MONGODB_URI = process.env.REMOTE_MONGODB_URI;
const IS_LOCAL_MONGODB = process.env.IS_LOCAL_MONGODB;
const MONGODB_URI = IS_LOCAL_MONGODB ? LOCAL_MONGODB_URI : REMOTE_MONGODB_URI;

if (!MONGODB_URI) {
  throw new Error(
    "Please define the MONGODB_URI environment variable inside .env.local"
  );
}

/**
 * Global is used here to maintain a cached connection across hot reloads
 * in development. This prevents connections growing exponentially
 * during API Route usage.
 */
let cached = global.mongoose;

if (!cached) {
  cached = global.mongoose = { conn: null, promise: null };
}

async function dbConnect() {
  if (cached.conn) {
    return cached.conn;
  }

  if (!cached.promise) {
    const opts = {
      bufferCommands: false,
    };

    cached.promise = mongoose.connect(MONGODB_URI, opts).then((mongoose) => {
      return mongoose;
    });
  }
  cached.conn = await cached.promise;
  return cached.conn;
}

export default dbConnect;
